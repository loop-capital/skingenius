/** Supported external platforms */
export type Platform = 'square' | 'vagaro';

/** Sync status for every unified record */
export type SyncStatus = 'synced' | 'pending' | 'conflict' | 'error' | 'ignored';

/** OAuth state for a connected merchant */
export type ConnectionStatus = 'connected' | 'disconnected' | 'refresh_needed' | 'revoked';

/** Generic externally-sourced IDs */
export interface PlatformIds {
  square?: string | null;
  vagaro?: string | null;
}

/** Timestamp metadata auto-generated by the gateway */
export interface SyncMeta {
  createdAt: string;           // ISO 8601
  updatedAt: string;           // ISO 8601
  lastSyncedAt: string | null; // ISO 8601
  syncStatus: SyncStatus;
  sourcePlatform: Platform;    // Which platform last modified this record
  version: number;             // Optimistic-locking / conflict detection
}

// ─────────────────────────────────────────────────────────────────────────────
// 1. UnifiedCustomer
// ─────────────────────────────────────────────────────────────────────────────

/** Normalised contact info (deduplicated across platforms) */
export interface UnifiedContact {
  email: string | null;
  phone: string | null;        // E.164 normalised
  phoneCountryCode: string | null;
}

/** A single customer entity unified across Square + Vagaro */
export interface UnifiedCustomer {
  id: string;                  // UUIDv4 — COLORgenius canonical ID

  // ── Identity ──
  firstName: string;
  lastName: string;
  displayName: string;
  dateOfBirth: string | null;  // ISO 8601 date
  gender: 'male' | 'female' | 'non_binary' | 'prefer_not_to_say' | null;
  notes: string;

  // ── Contact ──
  contact: UnifiedContact;
  secondaryEmails: string[];   // Historical / duplicate emails found
  secondaryPhones: string[];   // Historical / duplicate phones found

  // ── Platform linkage ──
  platformIds: PlatformIds;
  platformRaw: Record<Platform, Record<string, unknown>>; // Stash full payload

  // ── Salon context ──
  salonId: string;             // COLORgenius salon tenant
  firstVisitDate: string | null;
  lastVisitDate: string | null;
  totalVisits: number;
  lifetimeSpendCents: number;  // Normalised to minor currency units
  preferredStylistId: string | null;
  tags: string[];

  // ── Marketing / consent ──
  emailMarketingConsent: boolean;
  smsMarketingConsent: boolean;
  birthYearForMarketing: number | null; // Derived, not DOB

  // ── Merge metadata ──
  mergedFrom: string[];        // List of UnifiedCustomer IDs merged into this one
  mergeConfidence: number;       // 0.0–1.0 score from identity resolution

  // ── Sync ──
  sync: SyncMeta;
}

// ─────────────────────────────────────────────────────────────────────────────
// 2. UnifiedAppointment
// ─────────────────────────────────────────────────────────────────────────────

/** Appointment lifecycle states */
export type AppointmentStatus =
  | 'booked'
  | 'confirmed'
  | 'checked_in'
  | 'in_progress'
  | 'completed'
  | 'cancelled'
  | 'no_show'
  | 'rescheduled';

/** A single service line-item inside an appointment */
export interface AppointmentServiceLine {
  id: string;                  // UUID
  unifiedServiceId: string;    // FK → UnifiedService
  platformServiceId: string;   // Raw platform service ID
  serviceName: string;
  stylistId: string | null;    // FK → UnifiedEmployee
  stylistName: string | null;
  durationMinutes: number;
  priceCents: number;
  startTime: string;           // ISO 8601
  endTime: string;             // ISO 8601
  colorFormulaRef: string | null; // FK → ColorFormula (COLORgenius extension)
}

/** Unified appointment — bookable unit of work */
export interface UnifiedAppointment {
  id: string;                  // UUIDv4

  // ── Participants ──
  salonId: string;
  customerId: string;          // FK → UnifiedCustomer
  customerName: string;        // Denormalised for quick display

  // ── Timing ──
  startTime: string;           // ISO 8601
  endTime: string;             // ISO 8601
  timezone: string;            // IANA tz (e.g. America/New_York)
  checkedInAt: string | null;
  completedAt: string | null;

  // ── Services ──
  services: AppointmentServiceLine[];
  totalDurationMinutes: number;
  totalPriceCents: number;

  // ── Status ──
  status: AppointmentStatus;
  cancellationReason: string | null;
  cancellationSource: Platform | 'manual' | null;

  // ── Platform linkage ──
  platformIds: PlatformIds;
  platformRaw: Record<Platform, Record<string, unknown>>;

  // ── Salon extensions ──
  roomId: string | null;
  chairId: string | null;
  isRecurring: boolean;
  recurrenceRule: string | null; // iCal RRULE string
  parentAppointmentId: string | null; // For reschedules / recurrences

  // ── Sync ──
  sync: SyncMeta;
}

// ─────────────────────────────────────────────────────────────────────────────
// 3. UnifiedTransaction
// ─────────────────────────────────────────────────────────────────────────────

/** Transaction line-item types */
export type TransactionLineType = 'service' | 'product' | 'tip' | 'discount' | 'tax' | 'fee' | 'gift_card' | 'other';

/** Payment tender types */
export type TenderType = 'cash' | 'card' | 'gift_card' | 'store_credit' | 'check' | 'other' | 'external';

/** A single line on a receipt */
export interface TransactionLineItem {
  id: string;
  type: TransactionLineType;
  unifiedServiceId: string | null;   // FK → UnifiedService (if service)
  unifiedProductId: string | null;   // FK → UnifiedInventory (if product)
  name: string;                      // Denormalised display name
  quantity: number;
  unitPriceCents: number;
  discountCents: number;
  taxCents: number;
  totalCents: number;
  employeeId: string | null;         // FK → UnifiedEmployee (who performed/sold)
  colorFormulaRef: string | null;
}

/** Payment tender record */
export interface TransactionTender {
  id: string;
  type: TenderType;
  amountCents: number;
  tipCents: number;
  cardLastFour: string | null;
  cardBrand: string | null;          // visa, mastercard, etc.
  processedAt: string;               // ISO 8601
  externalTransactionId: string | null; // Square/Vagaro payment ID
}

/** Unified POS transaction / receipt */
export interface UnifiedTransaction {
  id: string;                        // UUIDv4

  // ── Context ──
  salonId: string;
  customerId: string | null;         // FK → UnifiedCustomer
  customerName: string | null;
  appointmentId: string | null;      // FK → UnifiedAppointment

  // ── Line items ──
  lines: TransactionLineItem[];
  tenders: TransactionTender[];

  // ── Totals ──
  subtotalCents: number;
  discountTotalCents: number;
  taxTotalCents: number;
  tipTotalCents: number;
  totalCents: number;

  // ── Refunds ──
  refundedTotalCents: number;
  refundLines: TransactionLineItem[];

  // ── Metadata ──
  receiptNumber: string;
  receiptUrl: string | null;
  invoiceId: string | null;
  note: string;

  // ── Platform linkage ──
  platformIds: PlatformIds;
  platformRaw: Record<Platform, Record<string, unknown>>;

  // ── Sync ──
  sync: SyncMeta;
}

// ─────────────────────────────────────────────────────────────────────────────
// 4. UnifiedInventory
// ─────────────────────────────────────────────────────────────────────────────

/** Inventory item types */
export type InventoryItemType = 'product' | 'color' | 'developer' | 'treatment' | 'tool' | 'retail' | 'other';

/** Stock tracking unit */
export interface InventorySku {
  sku: string;
  barcode: string | null;
  supplierSku: string | null;
}

/** Stock level at a specific location / salon */
export interface StockLevel {
  locationId: string;
  quantityOnHand: number;
  quantityReserved: number;    // For appointments / pending orders
  quantityAvailable: number;   // Computed: onHand - reserved
  reorderPoint: number;
  reorderQuantity: number;
  lastCountedAt: string | null;
}

/** Unified product / inventory item */
export interface UnifiedInventory {
  id: string;                        // UUIDv4

  // ── Identity ──
  name: string;
  description: string;
  type: InventoryItemType;
  categoryId: string | null;         // FK → UnifiedCategory
  brand: string | null;

  // ── SKUs ──
  identifiers: InventorySku;
  alternativeSkus: string[];         // Historical SKUs from merges

  // ── Pricing ──
  basePriceCents: number;
  costCents: number | null;          // Average / last cost
  msrpCents: number | null;

  // ── Stock ──
  stock: StockLevel[];               // Per-location tracking
  isTracked: boolean;                // false = non-tracked service/consumable

  // ── Variants ──
  variantName: string | null;        // e.g. "8oz", "32oz", "Light Ash Blonde"
  parentProductId: string | null;    // FK → UnifiedInventory (variant parent)
  variantAttributes: Record<string, string>; // { size: "8oz", shade: "5N" }

  // ── Salon context ──
  salonId: string;
  supplierId: string | null;
  supplierName: string | null;

  // ── Platform linkage ──
  platformIds: PlatformIds;
  platformRaw: Record<Platform, Record<string, unknown>>;

  // ── Sync ──
  sync: SyncMeta;
}

