/** Unified employee / stylist record */
export interface UnifiedEmployee {
  id: string;
  salonId: string;
  firstName: string;
  lastName: string;
  displayName: string;
  email: string | null;
  phone: string | null;
  role: 'owner' | 'manager' | 'stylist' | 'assistant' | 'receptionist' | 'other';
  specialties: ServiceCategory[];
  bio: string;
  isActive: boolean;
  platformIds: PlatformIds;
  sync: SyncMeta;
}

/** Platform connection record (OAuth token storage) */
export interface PlatformConnection {
  id: string;
  salonId: string;
  platform: Platform;
  status: ConnectionStatus;

  // OAuth tokens (encrypted at rest)
  accessToken: string;               // Encrypted
  refreshToken: string;              // Encrypted
  tokenExpiresAt: string;            // ISO 8601
  tokenScope: string[];

  // Webhook config
  webhookSecret: string | null;      // Encrypted — for signature verification
  webhookEndpointUrl: string | null; // Our endpoint registered with platform
  webhookEventsSubscribed: string[];

  // Merchant info
  platformMerchantId: string;
  platformMerchantName: string;
  platformLocationId: string | null;

  // Sync state
  lastSyncAt: string | null;
  lastSyncCursor: string | null;     // Pagination / delta token
  initialSyncCompleted: boolean;

  sync: SyncMeta;
}

/** Identity match candidate (intermediate resolution result) */
export interface IdentityMatchCandidate {
  confidence: number;                // 0.0–1.0
  sourceCustomerId: string;          // Platform customer ID
  targetUnifiedCustomerId: string;   // Existing unified record (or null for new)
  matchedFields: ('email' | 'phone' | 'name' | 'dob')[],
  fieldScores: {
    email: number;
    phone: number;
    name: number;
    dob: number;
  };
  recommendation: 'link' | 'merge' | 'create_new' | 'review';
}

/** Webhook envelope — normalised before queuing */
export interface WebhookEnvelope {
  id: string;                        // UUID — our idempotency key
  platform: Platform;
  eventType: string;                   // Normalised event name
  receivedAt: string;                  // ISO 8601
  payload: Record<string, unknown>;    // Raw JSON body
  signature: string | null;
  processedAt: string | null;
  processingResult: 'success' | 'error' | 'ignored' | 'pending' | null;
  retryCount: number;
  lastError: string | null;
}

/** Audit log entry for every sync operation */
export interface SyncAuditLog {
  id: string;
  timestamp: string;                   // ISO 8601
  salonId: string;
  platform: Platform;
  operation: 'push' | 'pull' | 'webhook' | 'resolve' | 'merge' | 'retry';
  entityType: 'customer' | 'appointment' | 'transaction' | 'inventory' | 'service' | 'employee';
  entityId: string;                  // Unified ID
  platformEntityId: string;          // External ID
  status: 'success' | 'error' | 'conflict' | 'skipped';
  details: Record<string, unknown>;
  errorMessage: string | null;
}

