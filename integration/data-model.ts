/** Service category taxonomy (salon-specific) */
export type ServiceCategory =
  | 'hair_color'
  | 'hair_cut'
  | 'hair_treatment'
  | 'hair_styling'
  | 'hair_extension'
  | 'hair_removal'
  | 'skin_treatment'
  | 'nail_service'
  | 'consultation'
  | 'other';

/** Service sub-type for colour work */
export type ColorServiceType =
  | 'full_color'
  | 'root_touchup'
  | 'balayage'
  | 'highlights'
  | 'double_process'
  | 'color_correction'
  | 'gloss_toner'
  | 'vivid_fashion'
  | 'other_color';

/** Processing requirements for a service */
export interface ServiceProcessing {
  averageDurationMinutes: number;
  minDurationMinutes: number;
  maxDurationMinutes: number;
  requiresPatchTest: boolean;
  patchTestHoursBefore: number | null;
}

/** Unified salon service — the thing clients book */
export interface UnifiedService {
  id: string;                        // UUIDv4

  // ── Identity ──
  name: string;
  description: string;
  category: ServiceCategory;
  colorType: ColorServiceType | null;

  // ── Pricing ──
  basePriceCents: number;
  variablePricing: boolean;          // e.g. colour by-the-ounce
  priceRangeCents: [number, number] | null; // [min, max] if variable

  // ── Duration ──
  processing: ServiceProcessing;

  // ── Booking rules ──
  bookableOnline: boolean;
  requiresConsultationFirst: boolean;
  maxGuestsPerSlot: number;        // 1 for most, >1 for group classes
  bufferMinutesAfter: number;        // Cleanup / reset time

  // ── Resources ──
  requiredRoomType: string | null;   // e.g. "color_bar", "shampoo_station"
  requiredEquipment: string[];       // e.g. ["steamer", "color_cart"]

  // ── Salon context ──
  salonId: string;
  employeeIds: string[];             // Which stylists can perform

  // ── Platform linkage ──
  platformIds: PlatformIds;
  platformRaw: Record<Platform, Record<string, unknown>>;

  // ── Sync ──
  sync: SyncMeta;
}

